int		ft_sqrt(int nb)
{
	int		i;
	int		sqrt;

	i = 1;
	sqrt = 0;
	while (i <= nb / 2)
	{
		sqrt = i * i;
		if (sqrt == nb)
			return (i);
		i++;
	}
	return (0);
}
