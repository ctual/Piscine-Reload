int		ft_iterative_factorial(int nb)
{
	int		res;
	int		i;

	res = 1;
	i = 1;
	if ((nb == 1) || (nb == 0))
		return (1);
	if ((nb < 0) || (nb > 12))
		return (0);
	while (i <= nb)
	{
		res = i * res;
		i++;
	}
	return (res);
}
